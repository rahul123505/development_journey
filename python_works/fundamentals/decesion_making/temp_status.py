degre = int(input("enter temperature"))

if degre <10:
    print("cold")

elif degre >=10  and degre<20:
    print("cool")   

elif degre >=20 and degre<25:
    print("pleasent")

elif degre >=25 and degre<30:
    print("warm")
elif degre >=30 and degre<35:
    print("hot")       
else:
    print("very hot")    





